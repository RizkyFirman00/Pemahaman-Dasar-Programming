void main() {
  var nilai = 'B';
  switch (nilai) {
    case 'A':
      {
        print('Sangat Baik');
      }
      break;
    case 'B':
      {
        print('Baik');
      }
      break;
    case 'C':
      {
        print('Cukup');
      }
      break;
    case 'D':
      {
        print('Kurang');
      }
      break;
    case 'E':
      {
        print('Belajar Dulu');
      }
      break;
    default:
      {
        print('Nilai yang anda masukkan salah');
      }
      break;
  }
}
